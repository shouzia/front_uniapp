        async onLoad(options) {
            let addressParams = {
                page: 1,
                limit: 100,
            }
            let addressData = await this.$api.page(`address`, addressParams);
            this.addressOptions = addressData.data.list;
            let xianhuaParams = {
                page: 1,
                limit: 100,
            }
            let xianhuaData = await this.$api.page(`xianhua`, xianhuaParams);
            this.xianhuaOptions = xianhuaData.data.list;
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let xianhuaOrderTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'xianhua_order_types',
            }
			let xianhuaOrderTypes = await this.$api.page(`dictionary`, xianhuaOrderTypesParams);
			this.xianhuaOrderTypesOptions = xianhuaOrderTypes.data.list
		/*下拉框*/
			let xianhuaOrderPaymentTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'xianhua_order_payment_types',
            }
			let xianhuaOrderPaymentTypes = await this.$api.page(`dictionary`, xianhuaOrderPaymentTypesParams);
			this.xianhuaOrderPaymentTypesOptions = xianhuaOrderPaymentTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`xianhuaOrder`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.xianhuaOrderId){
                this.ruleForm.xianhuaOrderId = options.xianhuaOrderId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            addressChange(e) {
                this.addressIndex = e.target.value
                this.ruleForm.addressName = this.addressOptions[this.addressIndex].addressName
                this.ruleForm.addressId = this.addressOptions[this.addressIndex].id
            },
            xianhuaChange(e) {
                this.xianhuaIndex = e.target.value
                this.ruleForm.xianhuaName = this.xianhuaOptions[this.xianhuaIndex].xianhuaName
                this.ruleForm.xianhuaId = this.xianhuaOptions[this.xianhuaIndex].id
            },
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            xianhuaOrderTypesChange(e) {
                this.xianhuaOrderTypesIndex = e.target.value
                this.ruleForm.xianhuaOrderValue = this.xianhuaOrderTypesOptions[this.xianhuaOrderTypesIndex].indexName
                this.ruleForm.xianhuaOrderTypes = this.xianhuaOrderTypesOptions[this.xianhuaOrderTypesIndex].codeIndex
            },
            xianhuaOrderPaymentTypesChange(e) {
                this.xianhuaOrderPaymentTypesIndex = e.target.value
                this.ruleForm.xianhuaOrderPaymentValue = this.xianhuaOrderPaymentTypesOptions[this.xianhuaOrderPaymentTypesIndex].indexName
                this.ruleForm.xianhuaOrderPaymentTypes = this.xianhuaOrderPaymentTypesOptions[this.xianhuaOrderPaymentTypesIndex].codeIndex
            },
